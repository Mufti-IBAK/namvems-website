// Helper function to get event image by category
export const getEventImageByCategory = (category: string): string => {
  switch (category.toLowerCase()) {
    case 'conference': return ONLINE_IMAGES.EVENTS.CONFERENCE;
    case 'study group': return ONLINE_IMAGES.EVENTS.STUDY_GROUP;
    case 'workshop': return ONLINE_IMAGES.EVENTS.WORKSHOP;
    case 'networking': return ONLINE_IMAGES.EVENTS.NETWORKING;
    default: return ONLINE_IMAGES.EVENTS.DEFAULT;
  }
};

// Helper function to get resource image by type
export const getResourceImageByType = (type: string): string => {
  switch (type.toLowerCase()) {
    case 'handbook': return ONLINE_IMAGES.RESOURCES.HANDBOOK;
    case 'guide': return ONLINE_IMAGES.RESOURCES.GUIDE;
    case 'video': return ONLINE_IMAGES.RESOURCES.VIDEO;
    case 'image': return ONLINE_IMAGES.RESOURCES.IMAGE;
    case 'research': return ONLINE_IMAGES.RESOURCES.RESEARCH;
    default: return ONLINE_IMAGES.RESOURCES.DEFAULT;
  }
};